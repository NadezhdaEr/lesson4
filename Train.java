package com.company.type;

public class Train {
       String train = null;
       int platform = 0;
       String van = null;
       int number = 0;
       int personal = 0;

       public Train(String train, int platform, String van, int number, int personal) {
              this.train = train;
              this.platform = platform;
              this.van = van;
              this.number = number;
              this.personal = personal;
       }
}
